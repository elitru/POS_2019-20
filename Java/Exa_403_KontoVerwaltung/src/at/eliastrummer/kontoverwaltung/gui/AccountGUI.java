    private void miAddUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miAddUserActionPerformed
        String result = JOptionPane.showInputDialog("Add user | Enter username");
        if(result.isEmpty()){
            return;
        }
        AccountUser user = new AccountUser(result, account, taOutput);
        if(userModel.exists(user)){
            return;
        }
        userModel.add(user);
    }//GEN-LAST:event_miAddUserActionPerformed

